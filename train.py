#------------------------------------------------------------------------------------------------------------------#
#   使用 Ultralytics 官方库进行 YOLO26 模型训练
#   支持 detect（目标检测）和 segment（实例分割）两种任务
#   所有训练逻辑由 Ultralytics 内部实现，本脚本仅做参数配置与分发
#------------------------------------------------------------------------------------------------------------------#
from pathlib import Path

from ultralytics import YOLO

from utils.training_plots import add_training_plot_callback


#------------------------------------------------------------------------------------------------------------------#
#   task: 指定训练任务类型
#   "detect"  — 目标检测，使用边界框进行预测
#   "segment" — 实例分割，使用多边形掩码进行预测
#   两者使用不同的模型头部和损失函数，由 Ultralytics 自动切换
#------------------------------------------------------------------------------------------------------------------#
task = "detect"
#------------------------------------------------------------------------------------------------------------------#
#   model_path: 指定模型权值文件路径
#   可以是本地 .pt 文件路径（如 model_data/yolo26n-seg.pt），
#   也可以是官方模型名称（如 "yolo11n.pt"），Ultralytics 会自动下载。
#   训练时若使用预训练权值，请指向对应的预训练模型。
"""
YOLO26 模型性能参数表
----------------------------------------------------------------------
Model    size    mAPval    mAPval    Speed          Speed           params    FLOPs
         (pixels) 50-95     50-95(e2e) CPU ONNX(ms)  T4 TensorRT10(ms) (M)       (B)
----------------------------------------------------------------------
YOLO26n  640     40.9      40.1      38.9 ± 0.7    1.7 ± 0.0       2.4       5.4
YOLO26s  640     48.6      47.8      87.2 ± 0.9    2.5 ± 0.0       9.5       20.7
YOLO26m  640     53.1      52.5      220.0 ± 1.4   4.7 ± 0.1       20.4      68.2
YOLO26l  640     55.0      54.4      286.2 ± 2.0   6.2 ± 0.2       24.8      86.4
YOLO26x  640     57.5      56.9      525.8 ± 4.0   11.8 ± 0.2      55.7      193.9
"""
#------------------------------------------------------------------------------------------------------------------#
model_path = "model_data/yolo26s.pt"
#------------------------------------------------------------------------------------------------------------------#
#   data_yaml: 数据集配置文件路径
#   该文件需包含以下信息：
#     - path: 数据集根目录（使用绝对路径）
#     - train: 训练集图片相对路径
#     - val: 验证集图片相对路径
#     - nc: 类别数量
#     - names: 类别名称列表
#   示例详见 datasets/datasets.yaml
#------------------------------------------------------------------------------------------------------------------#
data_yaml = "datasets/datasets.yaml"
#------------------------------------------------------------------------------------------------------------------#
#   input_shape: 网络输入尺寸，必须是 32 的倍数
#   较大的输入尺寸（如 640）有助于检测小目标，但会消耗更多显存。
#   训练时会根据此尺寸对图片进行 letterbox 缩放。
#------------------------------------------------------------------------------------------------------------------#
input_shape = [640, 640]
#------------------------------------------------------------------------------------------------------------------#
#   训练轮数（epochs）
#   模型完整遍历训练集的次数。轮数越多，模型拟合程度越高。
#   配合 early stopping（patience）使用时可适当设置较大值。
#------------------------------------------------------------------------------------------------------------------#
epochs = 200
#------------------------------------------------------------------------------------------------------------------#
#   batch: 批次大小
#   每次梯度更新时使用的样本数量。受显存大小限制。
#   较大的 batch 可以加速训练，但可能影响泛化性能。
#   GPU 显存不足时可适当降低此值。
#------------------------------------------------------------------------------------------------------------------#
batch = 64
#------------------------------------------------------------------------------------------------------------------#
#   device: 训练设备
#   0 表示第一张 GPU，0,1 表示多卡训练，"cpu" 使用 CPU 训练。
#   推荐使用 GPU 进行训练，CPU 训练速度极慢。
#------------------------------------------------------------------------------------------------------------------#
device = 0
#------------------------------------------------------------------------------------------------------------------#
#   num_workers: 数据加载线程数
#   用于数据预读取的进程数。Windows 下建议设为 0-4，Linux 可适当提高。
#   数值越大，数据加载越快，但会占用更多内存。
#------------------------------------------------------------------------------------------------------------------#
num_workers = 8
#------------------------------------------------------------------------------------------------------------------#
#   optimizer: 优化器选择
#   "auto" — Ultralytics 根据模型自动选择（推荐）
#   "SGD"  — 随机梯度下降，收敛稳定但速度较慢
#   "Adam" — 自适应矩估计，收敛快但可能过拟合
#   "AdamW" — Adam + 权重衰减，当前推荐的选择
#------------------------------------------------------------------------------------------------------------------#
optimizer = "auto"
#------------------------------------------------------------------------------------------------------------------#
#   lr0: 初始学习率
#   控制参数更新的步长。过大导致不收敛，过小导致收敛缓慢。
#   SGD 通常使用 0.01，Adam 通常使用 0.001。
#   optimizer="auto" 时 Ultralytics 会自动选择合适的学习率。
#------------------------------------------------------------------------------------------------------------------#
lr0 = 0.001
#------------------------------------------------------------------------------------------------------------------#
#   box / cls / dfl: 损失函数权重
#   官方默认值：box=7.5，cls=0.5，dfl=1.5
#   box 控制边界框回归损失权重，cls 控制分类损失权重，dfl 控制分布焦点损失权重。
#   如果更关注 abnormal/healthy 分类是否正确，可小幅提高 cls，例如 1.0 或 1.5。
#   不建议一次调得过大，否则可能降低定位质量或加重类别过拟合。
#------------------------------------------------------------------------------------------------------------------#
box = 7.5
cls = 0.5
dfl = 1.5
#------------------------------------------------------------------------------------------------------------------#
#   patience: Early Stopping 耐心值
#   验证集损失连续 patience 轮没有下降时，提前终止训练。
#   设为 0 表示不启用 early stopping。
#   可以有效防止过拟合并节省训练时间。
#------------------------------------------------------------------------------------------------------------------#
patience = 30
#------------------------------------------------------------------------------------------------------------------#
#   save_period: 模型保存间隔
#   每隔 save_period 轮保存一次模型权值到 runs/ 目录。
#   配合 val=True 使用，保存的权值包含验证集指标。
#------------------------------------------------------------------------------------------------------------------#
save_period = 10
#------------------------------------------------------------------------------------------------------------------#
#   amp: 混合精度训练
#   True  — 启用混合精度（FP16），可大幅降低显存占用并加速训练
#   False — 使用单精度（FP32），训练更稳定但更慢
#   在支持 Tensor Core 的 GPU 上效果更佳。
#------------------------------------------------------------------------------------------------------------------#
amp = True
#------------------------------------------------------------------------------------------------------------------#
#   cache: 数据集缓存策略
#   False — 不缓存，每次从磁盘读取（节省内存）
#   True  — 将图片缓存到内存，加速训练（需要较大内存）
#   "disk" — 缓存到磁盘，速度介于两者之间
#------------------------------------------------------------------------------------------------------------------#
cache = True
#------------------------------------------------------------------------------------------------------------------#
#   plots: 是否生成训练曲线图
#   True  — 在训练过程中保存 loss 曲线、混淆矩阵等图表
#   False — 不保存图表，节省磁盘空间
#------------------------------------------------------------------------------------------------------------------#
plots = True
#------------------------------------------------------------------------------------------------------------------#
#   val: 是否在训练过程中进行验证
#   True  — 每个 epoch 结束后在验证集上评估模型
#   False — 不进行验证，只关注训练集损失
#   建议保持 True，以便监控模型是否过拟合。
#------------------------------------------------------------------------------------------------------------------#
val = True
#------------------------------------------------------------------------------------------------------------------#
#   verbose: 是否输出详细信息
#   True  — 打印每个 batch 的损失和训练进度
#   False — 只打印 epoch 级别的汇总信息
#------------------------------------------------------------------------------------------------------------------#
verbose = True
#------------------------------------------------------------------------------------------------------------------#
#   官方保存路径参数
#   project=None 时，Ultralytics 使用默认路径 runs/<task>/<name>。
#   注意：不要设置 project="runs"，在本版本 Ultralytics 中，
#   相对 project 会生成 runs/<task>/runs/<name> 的多层嵌套目录。
#------------------------------------------------------------------------------------------------------------------#
project = None
#------------------------------------------------------------------------------------------------------------------#
#   name: 实验名称
#   运行结果的保存目录名，最终路径为 runs/<task>/<name>/。
#   如果同名目录已存在且 exist_ok=False，会自动添加序号后缀。
#------------------------------------------------------------------------------------------------------------------#
name = "train"
#------------------------------------------------------------------------------------------------------------------#
#   exist_ok: 是否允许覆盖已有运行结果
#   True  — 同名目录存在时直接覆盖
#   False — 同名目录存在时自动添加序号后缀（如 train2, train3...）
#------------------------------------------------------------------------------------------------------------------#
exist_ok = False
#------------------------------------------------------------------------------------------------------------------#
#   pretrained: 是否使用预训练权值
#   True  — 加载 model_path 指定的预训练权值进行迁移学习
#   False — 从头开始训练（随机初始化权重）
#   使用预训练权值可以显著加速收敛并提高最终精度。
#------------------------------------------------------------------------------------------------------------------#
pretrained = True
#------------------------------------------------------------------------------------------------------------------#
#   resume: 是否中断后恢复训练
#   设为 True 时，model_path 应指向 Ultralytics 保存的 last.pt 检查点。
#   恢复训练时会保留优化器状态、学习率调度器状态和当前轮数。
#   注意：resume=True 时无需设置 epochs，会沿用上次未完成的轮数。
#------------------------------------------------------------------------------------------------------------------#
resume = False
#------------------------------------------------------------------------------------------------------------------#
#   freeze: 冻结层设置
#   None     — 不冻结，训练所有层
#   整数     — 冻结前 N 层（如 freeze=10 冻结前 10 层）
#   列表     — 冻结指定索引的层（如 freeze=[0, 1, 2]）
#   冻结 backbone 层可以加快微调速度，防止灾难性遗忘。
#------------------------------------------------------------------------------------------------------------------#
freeze = None
#------------------------------------------------------------------------------------------------------------------#
#   class_weights: 类别权重
#   用于解决类别不平衡问题，为少数类赋予更高权重。
#
#   类别顺序：
#   - 索引 0: abnormal (病态类)
#   - 索引 1: healthy (健康类)
#
#   权重值通常根据类别样本数设置，例如：
#   - 如果健康类有 100 张，病态类有 20 张，可设置为 [5.0, 1.0]
#     （病态类权重 5 倍，健康类权重 1 倍）
#   - 权重值建议范围：1.0-10.0，避免过大导致训练不稳定
#   注意：当前官方 Ultralytics YOLO.train() 不支持 class_weights 参数。
#   保持 None 可继续使用官方训练流程；如需类别权重，需要自定义训练架构。
#   None 表示不使用类别权重（默认）
#------------------------------------------------------------------------------------------------------------------#
class_weights = None
#------------------------------------------------------------------------------------------------------------------#
#   以下为 Ultralytics 官方训练数据增强参数
#   本脚本仅将参数传递给 YOLO.train()，实际增强逻辑仍由 Ultralytics 内部实现。
#   下方"官方默认值"来自当前环境安装的 Ultralytics 配置。
#------------------------------------------------------------------------------------------------------------------#
#------------------------------------------------------------------------------------------------------------------#
#   hsv_h / hsv_s / hsv_v: HSV 色彩空间增强幅度
#   官方默认值：hsv_h=0.015，hsv_s=0.7，hsv_v=0.4
#   hsv_h 控制色相变化范围，hsv_s 控制饱和度变化范围，hsv_v 控制明度变化范围。
#   数值越大，颜色扰动越强，可提升光照、色彩变化下的泛化能力。
#   如果类别依赖颜色判断，或医学、工业、缺陷检测等颜色敏感数据集，可适当降低。
#------------------------------------------------------------------------------------------------------------------#
hsv_h = 0.015
hsv_s = 0.7
hsv_v = 0.4
#------------------------------------------------------------------------------------------------------------------#
#   degrees / translate / scale / shear / perspective: 几何变换增强
#   官方默认值：degrees=0.0，translate=0.1，scale=0.5，shear=0.0，perspective=0.0
#   degrees     — 随机旋转角度范围，0.0 表示不额外旋转
#   translate   — 随机平移比例，0.1 表示最多约 10% 图像尺寸的平移
#   scale       — 随机缩放比例，0.5 表示进行较强的尺度扰动
#   shear       — 随机错切角度，0.0 表示不做错切
#   perspective — 随机透视变换强度，0.0 表示不做透视变换
#   小目标、细长目标或分割边界敏感任务中，过强几何增强可能影响收敛。
#------------------------------------------------------------------------------------------------------------------#
degrees = 10.0
translate = 0.1
scale = 0.5
shear = 5.0
perspective = 0.001
#------------------------------------------------------------------------------------------------------------------#
#   flipud / fliplr: 随机翻转增强
#   官方默认值：flipud=0.0，fliplr=0.5
#   flipud 为上下翻转概率，fliplr 为左右翻转概率。
#   flipud=0.0 表示默认不做上下翻转，fliplr=0.5 表示约一半样本做左右翻转。
#   如果目标存在固定方向语义（如文字、上下结构、左右结构），应谨慎开启对应翻转。
#------------------------------------------------------------------------------------------------------------------#
flipud = 0.0
fliplr = 0.5
#------------------------------------------------------------------------------------------------------------------#
#   bgr: 随机 BGR 通道交换概率
#   官方默认值：bgr=0.0
#   控制随机交换颜色通道的概率，主要用于增加颜色通道鲁棒性。
#   bgr=0.0 表示默认关闭。普通自然图像、颜色敏感数据集通常保持默认即可。
#------------------------------------------------------------------------------------------------------------------#
bgr = 0.0
#------------------------------------------------------------------------------------------------------------------#
#   mosaic: Mosaic 数据增强概率
#   官方默认值：mosaic=1.0
#   将多张图片拼接训练，有助于提升小目标和复杂背景鲁棒性。
#   mosaic=1.0 表示训练前期默认启用 Mosaic；可配合 close_mosaic 在后期关闭。
#   对分割任务或目标边界要求较高的数据集，过强 Mosaic 可能带来边界噪声。
#------------------------------------------------------------------------------------------------------------------#
mosaic = 1.0
#------------------------------------------------------------------------------------------------------------------#
#   mixup: MixUp 数据增强概率
#   官方默认值：mixup=0.0
#   将两张图像及标签按比例混合，可提升泛化能力，但可能降低收敛速度。
#   mixup=0.0 表示默认关闭。数据量较少或过拟合明显时，可小幅尝试开启。
#------------------------------------------------------------------------------------------------------------------#
mixup = 0.2
#------------------------------------------------------------------------------------------------------------------#
#   copy_paste / copy_paste_mode: Copy-Paste 数据增强
#   官方默认值：copy_paste=0.0，copy_paste_mode="flip"
#   copy_paste 控制增强概率，copy_paste_mode 控制粘贴策略。
#   copy_paste=0.0 表示默认关闭，copy_paste_mode="flip" 表示使用翻转粘贴策略。
#   对实例分割任务较常用；检测任务、标签较少或目标边界很精细时建议先保持较低概率。
#------------------------------------------------------------------------------------------------------------------#
copy_paste = 0.0
copy_paste_mode = "flip"
#------------------------------------------------------------------------------------------------------------------#
#   auto_augment: 自动增强策略
#   官方默认值：auto_augment="randaugment"
#   Ultralytics 支持如 "randaugment"、"autoaugment"、"augmix" 等策略。
#   "randaugment" 会自动组合多种图像增强策略，通常用于提升泛化能力。
#   设为 None 可关闭该项；可用取值由当前安装的 Ultralytics 版本决定。
#------------------------------------------------------------------------------------------------------------------#
auto_augment = "randaugment"
#------------------------------------------------------------------------------------------------------------------#
#   erasing: 随机擦除概率
#   官方默认值：erasing=0.4
#   用于提升遮挡鲁棒性。过高可能破坏关键缺陷、小目标或分割区域。
#   erasing=0.4 表示默认有一定概率随机遮挡局部区域；分割边界敏感任务可适当降低。
#------------------------------------------------------------------------------------------------------------------#
erasing = 0.2
#------------------------------------------------------------------------------------------------------------------#
#   crop_fraction: 分类任务裁剪比例
#   官方默认值：crop_fraction=None
#   主要用于分类任务的裁剪比例。检测和分割训练通常不需要设置。
#   保持 None 时，本脚本会过滤该参数，不显式传入 YOLO.train()。
#------------------------------------------------------------------------------------------------------------------#
crop_fraction = None
#------------------------------------------------------------------------------------------------------------------#
#   close_mosaic: 最后若干 epoch 关闭 Mosaic
#   官方默认值：close_mosaic=10
#   通常用于训练后期让模型回到更接近真实图片分布的样本上微调。
#   close_mosaic=10 表示最后 10 个 epoch 关闭 Mosaic；设为 0 表示不主动关闭。
#------------------------------------------------------------------------------------------------------------------#
close_mosaic = 30


def require_existing_file(path, label):
    path_obj = Path(path)
    if not path_obj.is_file():
        raise FileNotFoundError(f"{label} not found: {path}")
    return str(path)


def build_train_kwargs(
    data_yaml,
    task,
    imgsz,
    epochs,
    batch,
    device,
    workers,
    project,
    name,
    exist_ok,
    pretrained,
    resume,
    optimizer,
    lr0,
    box,
    cls,
    dfl,
    patience,
    save_period,
    amp,
    freeze,
    cache,
    plots,
    val,
    verbose,
    hsv_h,
    hsv_s,
    hsv_v,
    degrees,
    translate,
    scale,
    shear,
    perspective,
    flipud,
    fliplr,
    bgr,
    mosaic,
    mixup,
    copy_paste,
    copy_paste_mode,
    auto_augment,
    erasing,
    crop_fraction,
    close_mosaic,
    class_weights,
):
    if class_weights is not None:
        raise ValueError(
            "class_weights is not supported by official Ultralytics YOLO.train(); "
            "keep class_weights=None or use a custom training architecture."
        )

    kwargs = {
        "data": data_yaml,
        "task": task,
        "imgsz": imgsz,
        "epochs": epochs,
        "batch": batch,
        "device": device,
        "workers": workers,
        "name": name,
        "exist_ok": exist_ok,
        "pretrained": pretrained,
        "resume": resume,
        "optimizer": optimizer,
        "lr0": lr0,
        "box": box,
        "cls": cls,
        "dfl": dfl,
        "patience": patience,
        "save_period": save_period,
        "amp": amp,
        "cache": cache,
        "plots": plots,
        "val": val,
        "verbose": verbose,
        "hsv_h": hsv_h,
        "hsv_s": hsv_s,
        "hsv_v": hsv_v,
        "degrees": degrees,
        "translate": translate,
        "scale": scale,
        "shear": shear,
        "perspective": perspective,
        "flipud": flipud,
        "fliplr": fliplr,
        "bgr": bgr,
        "mosaic": mosaic,
        "mixup": mixup,
        "copy_paste": copy_paste,
        "copy_paste_mode": copy_paste_mode,
        "auto_augment": auto_augment,
        "erasing": erasing,
        "crop_fraction": crop_fraction,
        "close_mosaic": close_mosaic,
    }
    if project is not None:
        kwargs["project"] = project
    if freeze is not None:
        kwargs["freeze"] = freeze
    return {key: value for key, value in kwargs.items() if value is not None}


def run_training():
    require_existing_file(data_yaml, "Dataset YAML")
    if any(sep in model_path for sep in ("/", "\\")):
        require_existing_file(model_path, "Model")

    model = YOLO(model_path)
    add_training_plot_callback(model)
    train_kwargs = build_train_kwargs(
        data_yaml=data_yaml,
        task=task,
        imgsz=input_shape[0],
        epochs=epochs,
        batch=batch,
        device=device,
        workers=num_workers,
        project=project,
        name=name,
        exist_ok=exist_ok,
        pretrained=pretrained,
        resume=resume,
        optimizer=optimizer,
        lr0=lr0,
        box=box,
        cls=cls,
        dfl=dfl,
        patience=patience,
        save_period=save_period,
        amp=amp,
        freeze=freeze,
        cache=cache,
        plots=plots,
        val=val,
        verbose=verbose,
        hsv_h=hsv_h,
        hsv_s=hsv_s,
        hsv_v=hsv_v,
        degrees=degrees,
        translate=translate,
        scale=scale,
        shear=shear,
        perspective=perspective,
        flipud=flipud,
        fliplr=fliplr,
        bgr=bgr,
        mosaic=mosaic,
        mixup=mixup,
        copy_paste=copy_paste,
        copy_paste_mode=copy_paste_mode,
        auto_augment=auto_augment,
        erasing=erasing,
        crop_fraction=crop_fraction,
        close_mosaic=close_mosaic,
        class_weights=class_weights,
    )
    return model.train(**train_kwargs)


if __name__ == "__main__":
    run_training()
