#------------------------------------------------------------------------------------------------------------------#
#   使用 Ultralytics 官方库进行 YOLO26 预测与模型导出
#   支持图片预测、视频检测、FPS 测试、目录批量预测和 ONNX 导出
#   所有预处理、后处理和渲染由 Ultralytics 内部实现
#------------------------------------------------------------------------------------------------------------------#

from ultralytics import YOLO

from utils.predict_runner import (
    build_common_predict_kwargs,
    build_export_kwargs,
    run_fps_test,
    run_interactive_predict,
    run_web_server,
)


#------------------------------------------------------------------------------------------------------------------#
#   mode: 运行模式选择
#   "predict"     — 单张图片预测（支持交互式输入）
#   "video"       — 视频文件或摄像头实时检测
#   "fps"         — 测试模型推理速度（FPS）
#   "dir_predict" — 批量预测目录下所有图片
#   "export_onnx" — 将 PyTorch 模型导出为 ONNX 格式
#   "web"         — 启动 Web 服务器，在浏览器上传图片并查看预测结果
#   根据需求选择对应的模式，不同模式使用下方对应的配置参数。
#------------------------------------------------------------------------------------------------------------------#
mode = "web"
#------------------------------------------------------------------------------------------------------------------#
#   model_path: 模型权值文件路径
#   可以是本地 .pt 文件路径，也可以是官方模型名称。
#   预测时使用的权值应与训练时的任务类型（detect/segment）一致。
#------------------------------------------------------------------------------------------------------------------#
model_path = r"models\best_8.pt"
#------------------------------------------------------------------------------------------------------------------#
#   input_shape: 网络输入尺寸，必须是 32 的倍数
#   预测时会将输入图片缩放到此尺寸进行推理。
#   较大的尺寸有助于检测小目标，但会降低推理速度。
#------------------------------------------------------------------------------------------------------------------#
input_shape = [640, 640]
#------------------------------------------------------------------------------------------------------------------#
#   confidence: 置信度阈值
#   只有置信度高于此值的检测框才会被保留。
#   值越高，预测结果越少但越精确（高精度低召回）。
#   值越低，预测结果越多但可能包含大量误检（低精度高召回）。
#   日常预测推荐 0.3~0.5，具体取决于任务对误检的容忍度。
#------------------------------------------------------------------------------------------------------------------#
confidence = 0.3
#------------------------------------------------------------------------------------------------------------------#
#   iou: NMS 非极大抑制的 IoU 阈值
#   用于去除重叠的检测框。值越大，对重叠的容忍度越高，保留的框越多。
#   推荐值：0.5~0.7。值越低，去重越严格，保留的框越少。
#------------------------------------------------------------------------------------------------------------------#
iou = 0.7
#------------------------------------------------------------------------------------------------------------------#
#   device: 推理设备
#   0 表示第一张 GPU，"cpu" 使用 CPU 推理。
#   如果 GPU 显存不足，可以设为 "cpu" 但速度会显著降低。
#------------------------------------------------------------------------------------------------------------------#
device = 0
#------------------------------------------------------------------------------------------------------------------#
#   verbose: 是否输出详细信息
#   True  — 打印推理过程的详细日志，包括每张图片的检测结果
#   False — 只输出最小化信息
#------------------------------------------------------------------------------------------------------------------#
verbose = True
#------------------------------------------------------------------------------------------------------------------#
#   官方保存路径参数
#   project=None 时，Ultralytics 使用默认路径 runs/<task>/<name>。
#   注意：不要设置 project="runs"，在本版本 Ultralytics 中，
#   相对 project 会生成 runs/<task>/runs/<name> 的多层嵌套目录。
#------------------------------------------------------------------------------------------------------------------#
project = None
#------------------------------------------------------------------------------------------------------------------#
#   name: 运行名称
#   预测结果的保存目录名，最终路径为 runs/<task>/<name>/。
#   如果同名目录已存在且 exist_ok=False，会自动添加序号后缀。
#------------------------------------------------------------------------------------------------------------------#
name = "predict"
#------------------------------------------------------------------------------------------------------------------#
#   exist_ok: 是否允许覆盖已有运行结果
#   True  — 同名目录存在时直接覆盖
#   False — 同名目录存在时自动添加序号后缀（如 predict2, predict3...）
#------------------------------------------------------------------------------------------------------------------#
exist_ok = False
#------------------------------------------------------------------------------------------------------------------#
#   save: 是否保存预测结果图片
#   True  — 将预测后的图片保存到 runs/ 目录
#   False — 仅在内存中处理，不保存结果
#------------------------------------------------------------------------------------------------------------------#
save = True
#------------------------------------------------------------------------------------------------------------------#
#   show: 是否实时显示预测结果
#   True  — 弹出窗口显示检测结果（需有图形界面环境）
#   False — 不显示窗口，仅在后台处理
#------------------------------------------------------------------------------------------------------------------#
show = True
#------------------------------------------------------------------------------------------------------------------#
#   display_window_size: show=True 时 OpenCV 预览窗口固定大小
#   仅影响 predict 模式下弹出的预览窗口，不影响保存图片的分辨率。
#------------------------------------------------------------------------------------------------------------------#
display_window_size = [960, 720]
#------------------------------------------------------------------------------------------------------------------#
#   save_txt: 是否保存标签文件
#   True  — 将预测结果保存为 YOLO 格式的 .txt 标签文件
#   False — 不保存标签文件，只保存图片
#------------------------------------------------------------------------------------------------------------------#
save_txt = False
#------------------------------------------------------------------------------------------------------------------#
#   save_conf: 是否在标签中包含置信度
#   True  — 在保存的 .txt 标签文件中包含每个框的置信度值
#   False — 只保存类别和坐标信息
#   仅在 save_txt=True 时生效。
#------------------------------------------------------------------------------------------------------------------#
save_conf = False
#------------------------------------------------------------------------------------------------------------------#
#   predict 模式配置
#   predict_source: 要预测的图片路径。为空字符串（""）时，
#   程序会进入交互模式，等待用户输入图片路径。
#   支持图片文件路径或目录路径。
#------------------------------------------------------------------------------------------------------------------#
predict_source = ""
#------------------------------------------------------------------------------------------------------------------#
#   video 模式配置
#   video_path: 视频文件路径或摄像头设备号。
#     0       — 使用默认摄像头实时检测
#     文件路径 — 检测指定视频文件
#   video_save_path: 结果视频保存路径。保留供用户编辑习惯；
#   官方保存位置由 project/name 控制。
#------------------------------------------------------------------------------------------------------------------#
video_path = 0
video_save_path = ""
#------------------------------------------------------------------------------------------------------------------#
#   fps 模式配置
#   test_interval: FPS 测试帧数，连续推理指定次数后取平均
#   fps_image_path: 用于测试的图片路径
#   通过多次推理取平均得到稳定的 FPS 数据。
#------------------------------------------------------------------------------------------------------------------#
test_interval = 100
fps_image_path = "img/street.jpg"
#------------------------------------------------------------------------------------------------------------------#
#   dir_predict 模式配置
#   dir_origin_path: 批量预测的图片目录路径
#   程序会遍历该目录下所有支持的图片格式进行预测。
#------------------------------------------------------------------------------------------------------------------#
dir_origin_path = "img/"
#------------------------------------------------------------------------------------------------------------------#
#   export_onnx 模式配置
#   simplify:  是否对导出的 ONNX 模型进行简化（去除冗余算子）
#   dynamic:   是否使用动态 batch 和动态输入尺寸
#   opset:     ONNX opset 版本号（None 表示使用默认版本）
#   导出的 ONNX 模型可用于部署到非 PyTorch 环境。
#------------------------------------------------------------------------------------------------------------------#
simplify = True
dynamic = False
opset = None
#------------------------------------------------------------------------------------------------------------------#
#   web 模式配置
#   web_host: Web 服务器监听地址
#   "127.0.0.1" — 仅本机可访问
#   "0.0.0.0"   — 局域网内其他设备也可访问
#   web_port: Web 服务器监听端口
#------------------------------------------------------------------------------------------------------------------#
web_host = "0.0.0.0"
web_port = 8081


def common_predict_kwargs(save):
    return build_common_predict_kwargs(
        input_shape=input_shape,
        confidence=confidence,
        iou=iou,
        device=device,
        save=save,
        show=show,
        save_txt=save_txt,
        save_conf=save_conf,
        name=name,
        exist_ok=exist_ok,
        verbose=verbose,
        project=project,
    )


def export_kwargs():
    return build_export_kwargs(
        input_shape=input_shape,
        simplify=simplify,
        dynamic=dynamic,
        opset=opset,
        device=device,
    )


def run_predict(model):
    return run_interactive_predict(
        model,
        predict_source,
        save,
        common_predict_kwargs,
        hold_show=show,
        display_window_size=tuple(display_window_size),
    )


def run_fps(model):
    return run_fps_test(model, test_interval, fps_image_path, common_predict_kwargs)


def run_web(model):
    return run_web_server(model, common_predict_kwargs, web_host, web_port)


def run_mode(selected_mode=mode):
    model = YOLO(model_path)

    if selected_mode == "predict":
        return run_predict(model)
    if selected_mode == "video":
        return model.predict(source=video_path, **common_predict_kwargs(save=save))
    if selected_mode == "fps":
        return run_fps(model)
    if selected_mode == "dir_predict":
        return model.predict(source=dir_origin_path, **common_predict_kwargs(save=True))
    if selected_mode == "export_onnx":
        return model.export(**export_kwargs())
    if selected_mode == "web":
        return run_web(model)

    raise AssertionError(
        "Please specify the correct mode: 'predict', 'video', 'fps', 'export_onnx', 'dir_predict', 'web'."
    )


if __name__ == "__main__":
    run_mode(mode)
