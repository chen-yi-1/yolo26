import glob
import os

import torch
from ultralytics import YOLO


if __name__ == "__main__":
    '''
    Recall和Precision不像AP是一个面积的概念，因此在门限值（Confidence）不同时，网络的Recall和Precision值是不同的。
    默认情况下，本代码计算的Recall和Precision代表的是当门限值（Confidence）为0.5时，所对应的Recall和Precision值。

    受到mAP计算原理的限制，网络在计算mAP时需要获得近乎所有的预测框，这样才可以计算不同门限条件下的Recall和Precision值
    因此，本代码获得的map_out/detection-results/里面的txt的框的数量一般会比直接predict多一些，目的是列出所有可能的预测框，
    '''
    #------------------------------------------------------------------------------------------------------------------#
    #   此处的model_path用于指定要评估的模型权值文件
    #   训练好后runs文件夹下存在多个权值文件，选择验证集损失较低的即可。
    #   验证集损失较低不代表mAP较高，仅代表该权值在验证集上泛化性能较好。
    #------------------------------------------------------------------------------------------------------------------#
    model_path      = "model_data/yolo26n.pt"
    #------------------------------------------------------------------------------------------------------------------#
    #   data_yaml用于指定数据集配置文件路径
    #   该文件需包含数据集路径、类别数、类别名称等信息
    #------------------------------------------------------------------------------------------------------------------#
    data_yaml       = 'datasets/datasets.yaml'
    #---------------------------------------------------------------------#
    #   input_shape     输入的shape大小，必须是32的倍数
    #---------------------------------------------------------------------#
    input_shape     = [640, 640]
    #--------------------------------------------------------------------------------------#
    #   受到mAP计算原理的限制，网络在计算mAP时需要获得近乎所有的预测框，这样才可以计算mAP
    #   因此，confidence的值应当设置的尽量小进而获得全部可能的预测框。
    #
    #   该值一般不调整。因为计算mAP需要获得近乎所有的预测框，此处的confidence不能随便更改。
    #   想要获得不同门限值下的Recall和Precision值，请修改下方的score_threhold。
    #--------------------------------------------------------------------------------------#
    confidence      = 0.001
    #--------------------------------------------------------------------------------------#
    #   预测时使用到的非极大抑制值的大小，越大表示非极大抑制越不严格。
    #
    #   该值一般不调整。
    #--------------------------------------------------------------------------------------#
    nms_iou         = 0.5
    #------------------------------------------------------#
    #   split       验证集或测试集 (val / test)
    #------------------------------------------------------#
    split           = "val"
    #-------------------------------#
    #   是否使用Cuda
    #   没有GPU可以设置成False
    #-------------------------------#
    device          = "cuda" if torch.cuda.is_available() else "cpu"

    model = YOLO(model_path)
    metrics = model.val(
        data=data_yaml,
        imgsz=input_shape[0],
        conf=confidence,
        iou=nms_iou,
        split=split,
        device=device,
        plots=True,
    )

    print("Validation metrics:")
    for key, value in metrics.results_dict.items():
        print(f"{key}: {value:.6f}")
    if hasattr(metrics, "seg"):
        print(f"mask mAP50-95: {metrics.seg.map:.6f}")
        print(f"mask mAP50: {metrics.seg.map50:.6f}")
    if hasattr(metrics, "box"):
        print(f"box mAP50-95: {metrics.box.map:.6f}")
        print(f"box mAP50: {metrics.box.map50:.6f}")
